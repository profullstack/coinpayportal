const COUNTRY_CODES = new Set(STRIPE_CONNECT_COUNTRIES.map((c) => c.code));

export function isSupportedConnectCountry(code: string | null | undefined): code is string {
  return typeof code === 'string' && COUNTRY_CODES.has(code.toUpperCase());
}

export function normalizeCountryCode(code: string | null | undefined): string | null {
  if (typeof code !== 'string') return null;
  const upper = code.trim().toUpperCase();
  return COUNTRY_CODES.has(upper) ? upper : null;
}
